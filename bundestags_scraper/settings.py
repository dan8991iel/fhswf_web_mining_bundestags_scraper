def find_root_with(folder_name: str) -> Path:
    here = Path(__file__).resolve()
    for parent in here.parents:
        if (parent / folder_name).is_dir():
            return parent
    raise FileNotFoundError(f"Could not find '{folder_name}' in any parent dirs")

